// Open Excel overlay modal
function openOverlay() {
    document.getElementById('overlayModal').style.display = 'flex';
}

// Close Excel overlay modal
function closeOverlay() {
    document.getElementById('overlayModal').style.display = 'none';
}

// Redirect for Excel
function redirectTo(option) {
    if (option === 'notes') {
        window.location.href = 'https://www.w3schools.com/excel/';
    } else if (option === 'youtube') {
        window.open('https://www.youtube.com/embed/Vl0H-qTclOg?si=BfUOw37UDdPBp53q', '_blank');
    }
}

